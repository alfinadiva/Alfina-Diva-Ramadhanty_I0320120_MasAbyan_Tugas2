#perhitungan luas persegi panjang
print("1. Menghitung Luas Persegi Panjang")

#input nilai panjang
panjang = float(input("Masukan panjang = "))

#input nilai lebar
lebar = float(input("Masukan lebar = "))

#rumus perhitungan luas persegi panjang
luas_persegi_panjang = panjang * lebar

#hasil perhitungan luas persegi panjang
print("luas persegi panjang= ", luas_persegi_panjang)


#perhitungan luas lingkaran
print("2. Menghitung Luas Lingkaran")

#input nilai jari-jari
r = float(input("Masukan nilai jari-jari=  "))

#rumus perhitungan luas lingkaran
luas_lingkaran = 3.14 * (r ** 2)

#hasil perhitungan luas lingkaran
print("luas lingkaran: ", luas_lingkaran)


#perhitungan luas kubus
print("3. Menghitung Luas Kubus")

#input nilai sisi
s = float(input("Masukan nilai sisi: "))

#rumus perhitungan luas kubus
luas_kubus = 6 * (s ** 2)

#hasil perhitungan luas kubus
print("luas kubus = ", luas_kubus)

